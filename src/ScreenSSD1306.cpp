    void init(void){

        // SSD1306_SWITCHCAPVCC = generate display voltage from 3.3V internally
        if (!display.begin(SSD1306_SWITCHCAPVCC, SCREEN_ADDRESS))
        {
            Serial.println(F("SSD1306 allocation failed"));
            for (;;)
                ; // Don't proceed, loop forever
        }
        // Clear the buffer
        display.clearDisplay();

        println("Display Text");
        testtext(); // Draw scrolling text

        println("Display Logo");
        testdrawbitmap(); // Draw a small bitmap image
    }

    void testtext(void) {
    display.clearDisplay();

    // for size 1, max number of letter for the screen width is 21 (128 / 6)
    // for size 1, max line for the screen height is 10 (64 / 8)
    // for size 2, max number of letter for the screen width is 10 (128 / 12)
    // for size 2, max line for the screen height is 5 (64 / 16)
    // for size 3, max number of letter for the screen width is 7 (128 / 18)
    // for size 3, max line for the screen height is 3 (64 / 24)
    // for size 4, max number of letter for the screen width is 5 (128 / 24)
    // for size 4, max line for the screen height is 2 (64 / 32)

    String text = "Mecapi";
    uint8_t textSize = 3;
    display.setTextSize(textSize);
    display.setTextColor(SSD1306_WHITE);
    display.setCursor((display.width() - (text.length()-1) * 6 * textSize) / 2, (display.height() - 8 * textSize) / 2); // Center the text horizontally and vertically
    display.print(text);
    display.display();      // Show initial text
    delay(3000);
    }

    void testdrawbitmap(void) {
    display.clearDisplay();

    display.drawBitmap(
        (display.width()  - logo_bmp_width ) / 2,
        (display.height() - logo_bmp_height) / 2,
        logo_bmp, logo_bmp_width, logo_bmp_height, 1);
    display.display();
    delay(1000);
    }

